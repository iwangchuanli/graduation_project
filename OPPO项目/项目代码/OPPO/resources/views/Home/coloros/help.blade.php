							<li><a href="javascript:void(0);">ColorOS 3.0为什么找不到网盘和云相册的功能了？</a>
								<p>ColorOS云服务主要解决用户更换手机重要信息的转移和同步，所以可以将联系人、电话、笔记等内容备份到云端，已经满足大多数云服务使用的需要了。
									网盘和云相册用来储存文件，受限于当前普遍的网速和商务合作的限制，使用体验并不理想，所以没有提供网盘和云相册功能。
									原来储存在云相册的图片没有丢失，可通过登录http://yun.oppo.com查看和下载到本地。
								</p></li>
							<li><a href="javascript:void(0);">ColorOS 3.0为什么没有了流量较准功能？</a>
								<p>现在4G流量非常复杂，除了省内流量、全国流量、闲时流量、半年流量包、还有上月转存流量。而运营商没有提供这种流量查询的接口，所以不能够准确知道一个月能用的流量还有多少了。
									你可以在设置-双卡与移动网络”中，设置好每日流量限额，打开提醒功能。比如设置一天超过30M就断网并提醒，这样可以有效避免第三方应用偷跑流量。使用起来简单方便，流量使用更放心。
								</p></li>
							<li><a href="javascript:void(0);">ColorOS 3.0为什么不能直接显示SIM卡联系人？</a>
								<p>如果想要使用SIM联系人，只需要进入“设置-电话本-SIM卡联系人”，将SIM卡保存的联系人复制到手机中，就可以在手机上使用联系人了。
									ColorOS 3.0不直接显示SIM卡联系人主要基于以下几个原因：<br>
									1.SIM卡本身读写速度慢，开机后从SIM卡读取联系人和保存联系人到SIM卡里非常慢，现在大家常常有上百个联系人，从SIM卡保存和读取太慢了，使用起来体验很不好；<br>
									2.SIM卡联系人只能保存姓名和号码，最新款的USIM卡可保存邮箱，但相比手机里存储的联系人，没有头像，不能设置联系人铃声，也不能记录联系人生日等等，不能满足大家妥善管理人脉的需求；<br>
									3.联系人存在SIM卡也是不安全的，如果SIM损坏，联系人就丢失了；而手机里存储的联系人可以备份到本地和云端，再也不用担心意外丢失。
								</p></li>
							<li><a href="javascript:void(0);">ColorOS 3.0为什么取消了菜单Menu键？</a>
								<p>通过研究大多数用户使用习惯，发现进入多任务界面管理后台和切换应用是最常用的操作，而原本“长按Home键”不够直观快捷，不能满足用户使用习惯的改变。所以我们将Menu键替换为多任务功能键。
									最新的Android默认也取消了Menu键，替换为多任务键，很多第三方应用也已经将Menu键的功能直接在应用中直观呈现出来，系统自带应用也遵循了这个设计原则，因此Menu键越来越没有必要。
								</p></li>
							<li><a href="javascript:void(0);">为什么ColorOS 3.0桌面没有插件？</a>
								<p>桌面支持插件，只是默认没有放置插件。你可以在桌面双指捏合进入桌面编辑模式添加插件。
									ColorOS 3.0 对桌面插件设计进行了精简优化：
									旧版本时间插件上的时间和天气信息，在3.0里都可以状态栏，以及最新的时钟和天气的动态图标中直观的看到。另外精简插件会让整个桌面更简洁直观。
								</p></li>
							<li><a href="javascript:void(0);">为什么我在音乐/电话等等各个应用功能里找不到设置？</a>
								<p>这些应用都可以设置的。
									每个应用常用的功能可以在应用界面里直接使用，而不常用设置项都统一统一集中到“设置”功能中统一调整，这样就不用一个个去点开应用挨个设置了。
									另外，这些不常用的设置项在第一次使用手机的时候，可以一次性在“设置”中调整，更加快捷好用。
								</p></li>
							<li><a href="javascript:void(0);">为什么ColorOS 3.0没有“安全中心”了？</a>
								<p>有的，只是更名了。
									ColorOS 3.0对之前的“安全中心”功能进行重新调整与优化，更名为“手机管家”。
									新的“手机管家”大幅提升了体检、清理加速、病毒权限核心功能的效能，特别是垃圾清理能力大幅提升，甚至可以针对微信、QQ这样有很多碎片文件的应用进行彻底的清扫，清理大量垃圾文件的同时不丢失聊天信息和资料。之前“安全中心”的电量管理和网络控制，现在都可以在“设置”应用中找到和调整。
								</p></li>
							<li><a href="javascript:void(0);">手机为什么不能进行VOOC闪充？</a>
								<p>1、手机必须是与专用的闪充充电器或闪充移动电源配套使用（MicroUSB 插头是7 pin针脚）；<br>
									2、手机的电池温度必须在15.5℃ ~ 42℃ 范围内，超出此温度范围时，手机均自动切换回标准充电模式（充电电流限制在2A以内）； <br>
									3、电池电压需大于3.55V、低于4.32V（ 电量显示在1%~85%之间；由于受电池性能衰减的影响，导致内阻波动，所以不能完全以电池电量来判定） 凡不符合以上充电条件的情况，系统均将自动调整为常规的标准充电模式，充电电流限制在 2A 以内。 可以进入拨号盘输入*#808#进入charger（充电器）截图，看一下是否符合以上条件。</p></li>
							<li><a href="javascript:void(0);">OPPO手机怎么截屏？</a>
								<p>OPPO手机有2种截屏方式：<br>
									1、基础操作：同时按住电源键+音量减键，保持1秒即可截屏；<br>
									2、ColorOS独有操作：进入手机设置--手势体感—快捷手势/亮屏手势--三指截屏，打开开关后即可在屏幕通过三指滑动截屏。
								</p></li>
						</ul>
