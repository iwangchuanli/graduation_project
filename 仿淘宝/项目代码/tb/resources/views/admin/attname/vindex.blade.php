	<script>
		$(function(){
			//删除属性值操作
			$('.atvdelete').bind('click',function(){
				
				//获取删除的属性名id
				var atvid = $(this).attr('data-id');
				//ajax传送到服务器
				if(!confirm('您确定要删除id为:'+atvid+'属性吗')){
					return false;
				};
				var $btn = $(this);
				$.ajax({
					url:'/admin/attvalue/delete',
					data:'atvid='+atvid,
					type:'get',
					dataType:'text',
					success:function(data){
						//根据返回结果判断是否删除成功
						if(data == 1){
							//清空当前行
							//找到tr标签进行删除操作
							$btn.parents('tr').remove();
						}else{
							//失败警告
							alert('删除失败');
						}
					}
				});
				//接受结果
			});
		});
	</script>
